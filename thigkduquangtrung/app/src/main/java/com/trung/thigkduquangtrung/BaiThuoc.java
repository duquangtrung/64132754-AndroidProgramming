package com.trung.thigkduquangtrung;

public class BaiThuoc {

    String ten;
    String thoigian;

    // Constructor
    public BaiThuoc(String ten, String thoigian) {
        this.ten = ten;
        this.thoigian = thoigian;
    }

    // Getter
    public String getTen() {
        return ten;
    }

    public String getThoigian() {
        return thoigian;
    }
}